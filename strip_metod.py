# strip method: it will use to remove space in String
name='   durgesh     '
do='..........'

# lstrip() method: use to remove left side space



# rstrip() method: use to remove right side space

print((name.lstrip()).rstrip()+do)

print(name.strip()+do)

# replace method: it will be used to replace one thing to new thing


nam='   dur    gesh     '
print(nam.replace(' ', ''))